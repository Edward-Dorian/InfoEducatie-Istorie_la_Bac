document.getElementById('cuprins').addEventListener('click', function(){
    var elements = document.getElementsByClassName('al');
    for(var i = 0; i < elements.length; i++) {
        if(elements[i].style.display == 'none') {
            elements[i].style.display = 'block';
            Object.assign(document.getElementById('pdf').style, {width: '85%'});
        } else {
            elements[i].style.display = 'none';
            Object.assign(document.getElementById('pdf').style, {width: '100%'});
        }
    }
});
    
